# news/tests/test_logic.py
"""
Тесты бизнес‑логики работы с комментариями в приложении YaNews.

Проверяемые сценарии:
* создание комментариев (авторизованными и анонимными пользователями);
* фильтрация стоп‑слов в комментариях;
* редактирование комментариев (автором и другими пользователями);
* удаление комментариев (автором и другими пользователями).
"""

from http import HTTPStatus

from django.contrib.auth import get_user_model  # type: ignore
from django.test import Client, TestCase  # type: ignore
from django.urls import reverse  # type: ignore

# Импортируем из файла с формами список стоп-слов и предупреждение формы.
# Загляните в news/forms.py, разберитесь с их назначением.
from news.forms import BAD_WORDS, WARNING
from news.models import Comment, News

User = get_user_model()


class TestCommentCreation(TestCase):
    """
    Набор тестов для проверки создания комментариев к новостям.

    Проверяет:
    * невозможность создания комментария анонимным пользователем;
    * успешное создание комментария авторизованным пользователем;
    * блокировку комментариев, содержащих стоп‑слова.
    """

    # Текст комментария понадобится в нескольких местах кода,
    # поэтому запишем его в атрибуты класса.
    COMMENT_TEXT = 'Текст комментария'

    @classmethod
    def setUpTestData(cls):
        """
        Подготовка тестовых данных для всех тестов класса.

        Создаёт:
        * тестовую новость;
        * пользователя и авторизованный клиент;
        * данные формы для создания комментария.

        Сохраняет URL страницы новости в атрибут класса.
        """
        cls.news = News.objects.create(title='Заголовок', text='Текст')
        # Адрес страницы с новостью.
        cls.url = reverse('news:detail', args=(cls.news.id,))
        # Создаём пользователя и клиент, логинимся в клиенте.
        cls.user = User.objects.create(username='Мимо Крокодил')
        cls.auth_client = Client()
        cls.auth_client.force_login(cls.user)
        # Данные для POST-запроса при создании комментария.
        cls.form_data = {'text': cls.COMMENT_TEXT}

    def test_anonymous_user_cant_create_comment(self):
        """
        Тест невозможности создания комментария анонимным пользователем.

        Проверяет, что:
        * анонимный пользователь не может отправить комментарий;
        * после попытки отправки количество комментариев
        в базе не изменяется (остаётся 0).

        Шаги:
        1. Выполняет POST‑запрос от анонимного клиента с данными комментария.
        2. Считает количество комментариев в БД.
        3. Сравнивает с ожидаемым значением (0).
        """
        # Совершаем запрос от анонимного клиента, в POST-запросе отправляем
        # предварительно подготовленные данные формы с текстом комментария.
        self.client.post(self.url, data=self.form_data)
        # Считаем количество комментариев.
        comments_count = Comment.objects.count()
        # Ожидаем, что комментариев в базе нет - сравниваем с нулём.
        self.assertEqual(comments_count, 0)

    def test_user_can_create_comment(self):
        """
        Тест успешного создания комментария авторизованным пользователем.

        Проверяет, что:
        * авторизованный пользователь может создать комментарий;
        * происходит редирект на раздел с комментариями;
        * в БД создаётся один комментарий;
        * атрибуты созданного комментария (текст, новость, автор)
        соответствуют ожидаемым.

        Шаги:
        1. Выполняет POST‑запрос от авторизованного клиента.
        2. Проверяет редирект.
        3. Считает комментарии в БД.
        4. Получает созданный комментарий и проверяет его атрибуты.
        """
        # Совершаем запрос через авторизованный клиент.
        response = self.auth_client.post(self.url, data=self.form_data)
        # Проверяем, что редирект привёл к разделу с комментами.
        self.assertRedirects(response, f'{self.url}#comments')
        # Считаем количество комментариев.
        comments_count = Comment.objects.count()
        # Убеждаемся, что есть один комментарий.
        self.assertEqual(comments_count, 1)
        # Получаем объект комментария из базы.
        comment = Comment.objects.get()
        # Проверяем, что все атрибуты комментария совпадают с ожидаемыми.
        self.assertEqual(comment.text, self.COMMENT_TEXT)
        self.assertEqual(comment.news, self.news)
        self.assertEqual(comment.author, self.user)

    def test_user_cant_use_bad_words(self):
        """
        Тест блокировки комментариев со стоп‑словами.

        Проверяет, что:
        * форма возвращает ошибку при наличии стоп‑слова;
        * комментарий не создаётся в БД;
        * отображается предупреждение (WARNING).

        Шаги:
        1. Формирует данные с текстом, содержащим стоп‑слово.
        2. Отправляет POST‑запрос от авторизованного клиента.
        3. Проверяет наличие ошибки в форме.
        4. Убеждается, что количество комментариев не изменилось.
        """
        # Формируем данные для отправки формы; текст включает
        # первое слово из списка стоп-слов.
        bad_words_data = {'text': f'Какой-то текст, {BAD_WORDS[0]}, еще текст'}
        # Отправляем запрос через авторизованный клиент.
        response = self.auth_client.post(self.url, data=bad_words_data)
        form = response.context['form']
        # Проверяем, есть ли в ответе ошибка формы.
        self.assertFormError(
            form=form,
            field='text',
            errors=WARNING
        )
        # Дополнительно убедимся, что комментарий не был создан.
        comments_count = Comment.objects.count()
        self.assertEqual(comments_count, 0)


class TestCommentEditDelete(TestCase):
    """
    Набор тестов для проверки редактирования и удаления комментариев.

    Проверяет:
    * возможность автора редактировать и удалять свои комментарии;
    * невозможность редактирования/удаления чужих комментариев;
    * корректность редиректов после операций.
    """

    # Тексты для комментариев не нужно дополнительно создавать
    # (в отличие от объектов в БД), им не нужны ссылки на self или cls,
    # поэтому их можно перечислить просто в атрибутах класса.
    COMMENT_TEXT = 'Текст комментария'
    NEW_COMMENT_TEXT = 'Обновлённый комментарий'

    @classmethod
    def setUpTestData(cls):
        """
        Подготовка тестовых данных для всех тестов класса.

        Создаёт:
        * тестовую новость;
        * двух пользователей (автора комментария и читателя);
        * авторизованных клиентов для обоих пользователей;
        * тестовый комментарий (автор — первый пользователь);
        * URL для редактирования и удаления комментария;
        * данные для обновления комментария.

        Сохраняет ключевые URL и объекты в атрибуты класса.
        """
        # Создаём новость в БД.
        cls.news = News.objects.create(title='Заголовок', text='Текст')
        # Формируем адрес блока с комментариями
        news_url = reverse('news:detail', args=(cls.news.id,))  # Адрес новости
        cls.url_to_comments = news_url + '#comments'  # Блока с комментариями.
        # Создаём пользователя - автора комментария.
        cls.author = User.objects.create(username='Автор комментария')
        # Создаём клиент для пользователя-автора.
        cls.author_client = Client()
        # "Логиним" пользователя в клиенте.
        cls.author_client.force_login(cls.author)
        # Делаем всё то же самое для пользователя-читателя.
        cls.reader = User.objects.create(username='Читатель')
        cls.reader_client = Client()
        cls.reader_client.force_login(cls.reader)
        # Создаём объект комментария.
        cls.comment = Comment.objects.create(
            news=cls.news,
            author=cls.author,
            text=cls.COMMENT_TEXT
        )
        # URL для редактирования комментария.
        cls.edit_url = reverse('news:edit', args=(cls.comment.id,))
        # URL для удаления комментария.
        cls.delete_url = reverse('news:delete', args=(cls.comment.id,))
        # Формируем данные для POST-запроса по обновлению комментария.
        cls.form_data = {'text': cls.NEW_COMMENT_TEXT}

    def test_author_can_delete_comment(self):
        """
        Тест возможности автора удалить свой комментарий.

        Проверяет, что:
        * автор может удалить свой комментарий;
        * происходит редирект на раздел с комментариями;
        * статус ответаравен HTTPStatus.FOUND (302);
        * количество комментариев в БД становится равным 0.

        Шаги:
        1. Выполняет DELETE‑запрос от клиента автора.
        2. Проверяет редирект на раздел с комментариями.
        3. Проверяет статус ответа.
        4. Считает количество комментариев в БД.
        5. Сравнивает с ожидаемым значением (0).
        """
        # От имени автора комментария отправляем DELETE-запрос на удаление.
        response = self.author_client.delete(self.delete_url)
        # Проверяем, что редирект привёл к разделу с комментариями.
        self.assertRedirects(response, self.url_to_comments)
        # Заодно проверим статус-коды ответов.
        self.assertEqual(response.status_code, HTTPStatus.FOUND)
        # Считаем количество комментариев в системе.
        comments_count = Comment.objects.count()
        # Ожидаем ноль комментариев в системе.
        self.assertEqual(comments_count, 0)

    def test_user_cant_delete_comment_of_another_user(self):
        """
        Тест невозможности удаления чужого комментария.

        Проверяет, что:
        * пользователь не может удалить комментарий другого пользователя;
        * сервер возвращает ошибку 404 (HTTPStatus.NOT_FOUND);
        * комментарий остаётся в базе данных (количество комментариев
        не меняется).

        Шаги:
        1. Выполняет DELETE‑запрос от клиента читателя (не автора).
        2. Проверяет статус ответа (404).
        3. Считает количество комментариев в БД.
        4. Сравнивает с ожидаемым значением (1).
        """
        # Выполняем запрос на удаление от пользователя-читателя.
        response = self.reader_client.delete(self.delete_url)
        # Проверяем, что вернулась 404 ошибка.
        self.assertEqual(response.status_code, HTTPStatus.NOT_FOUND)
        # Убедимся, что комментарий по-прежнему на месте.
        comments_count = Comment.objects.count()
        self.assertEqual(comments_count, 1)

    def test_author_can_edit_comment(self):
        """
        Тест возможности автора редактировать свой комментарий.

        Проверяет, что:
        * автор может обновить текст своего комментария;
        * происходит редирект на раздел с комментариями;
        * текст комментария в БД обновляется до нового значения.

        Шаги:
        1. Выполняет POST‑запрос на редактирование от клиента автора.
        2. Проверяет редирект.
        3. Обновляет объект комментария из БД.
        4. Сравнивает текст комментария с новым ожидаемым значением.
        """
        # Выполняем запрос на редактирование от имени автора комментария.
        response = self.author_client.post(self.edit_url, data=self.form_data)
        # Проверяем, что сработал редирект.
        self.assertRedirects(response, self.url_to_comments)
        # Обновляем объект комментария.
        self.comment.refresh_from_db()
        # Проверяем, что текст комментария соответствует обновленному.
        self.assertEqual(self.comment.text, self.NEW_COMMENT_TEXT)

    def test_user_cant_edit_comment_of_another_user(self):
        """
        Тест невозможности редактирования чужого комментария.

        Проверяет, что:
        * пользователь не может редактировать комментарий другого пользователя;
        * сервер возвращает ошибку 404 (HTTPStatus.NOT_FOUND);
        * текст комментария в БД остаётся неизменным.

        Шаги:
        1. Выполняет POST‑запрос на редактирование от клиента читателя.
        2. Проверяет статус ответа (404).
        3. Обновляет объект комментария из БД.
        4. Сравнивает текст комментария с исходным значением.
        """
        # Выполняем запрос на редактирование от имени другого пользователя.
        response = self.reader_client.post(self.edit_url, data=self.form_data)
        # Проверяем, что вернулась 404 ошибка.
        self.assertEqual(response.status_code, HTTPStatus.NOT_FOUND)
        # Обновляем объект комментария.
        self.comment.refresh_from_db()
        # Проверяем, что текст остался тем же, что и был.
        self.assertEqual(self.comment.text, self.COMMENT_TEXT)
