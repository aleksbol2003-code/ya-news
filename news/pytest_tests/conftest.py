    today = datetime.today()
    for index in range(11):
        news = News.objects.create(
            title=f'Новость №{index + 1}',
            text=f'Текст новости №{index + 1}. Содержимое тестовой новости.',
            date=today - timedelta(days=index)
        )
        news_list.append(news)
    return news_list


